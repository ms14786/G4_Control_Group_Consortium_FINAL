## HOME ##
-- How many ongoing experiments, researchers, institutions, and facilities are involved?
SELECT COUNT(experiment.experimentId) FROM experiment;
SELECT COUNT(researcher.researcherId) FROM researcher;
SELECT COUNT(institution.instId) FROM institution;
SELECT COUNT(facility.facId) FROM facility;

-- What are the most recent experiments by start date?
SELECT e.experimentId, e.experimentTitle, e.experimentStatus,
               r.researcherName
FROM experiment e
JOIN researcher r ON e.leadResearcherId = r.researcherId
ORDER BY e.globalStartDate DESC LIMIT 5;

## ALL EXPERIMENTS##
-- What are the names, lead researchers, and details of each experiment? 
SELECT e.experimentId, e.experimentTitle, e.experimentStatus,
               e.globalStartDate, e.globalEndDate,
               r.researcherName, i.instName
FROM experiment e
JOIN researcher r ON e.leadResearcherId = r.researcherId
JOIN institution i ON r.instId = i.instId
ORDER BY e.experimentId;

